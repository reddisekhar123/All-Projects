package sekhar_Jdbc;

public class Employee1 {
	
		private int empId;
		private String name;
		private int salary;
		private String dept;
		
		public int getEmpId() {
			return empId;
		}
		
		public void setEmpId(int empId) {
			this.empId=empId;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name=name;
		}
		public void setSalary(int salary) {
			this.salary=salary;
		}
		public int getSalary() {
			return salary;
		}
		public void setDept(String dept) {
			this.dept=dept;
		}
		public String getDept() {
			return dept;
		
	}
}

